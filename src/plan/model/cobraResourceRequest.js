export default class CobraResourceRequest {
    constructor() {
        this.resetData()
    }
    resetData() {
        this.IncidentName = ''
        this.Details = ''
        this.Title = ''
        this.Status = ''
        this.Priority = ''
        this.RequestType = ''
        this.Organization = ''
        this.Status = 0
        this.Priority = 0
    }
}
